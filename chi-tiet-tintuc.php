		Rau quả là thực phẩm không thể thiếu trong bữa ăn hàng ngày. Chất lượng của chúng sẽ ảnh hưởng lớn đến sức khỏe. <br>

Theo TS Hoàng Kim Thanh - Viện Dinh Dưỡng quốc gia - rau quả có vai trò đặc biệt quan trọng trong bữa ăn hàng ngày bởi cung cấp cho cơ thể nhiều muối khoáng, các vitamin, chất pectin và axit hữu cơ.<br>

Nhu cầu của con người về vitamin C, carotene chủ yếu do rau quả cung cấp. Ngoài ra, chúng còn có loại đường tan trong nước và xenluloza (có khả năng bài xuất cholesterol và chất độc ra khỏi cơ thể).<br>

Ngoài ra, rau quả còn có các men ảnh hưởng tốt tới quá trình tiêu hóa. Xenluloza ở dưới dạng liên kết với các chất pectin tạo thành phức hợp pectin - enluloza. Phức chất này kích thích mạnh chức năng nhu động ruột và tiết dịch ruột.<br>



Nguyên tắc chọn rau quả<br>

- Không nên mua các loại rau quả trái vụ, lúc này thời tiết không thuận lợi nên sâu bọ phát triển nhiều, rau quả cằn cỗi nên người trồng rau phải dùng nhiều loại hóa chất bảo vệ thực vật, thuốc kích thích tăng trưởng.<br>

- Không nên chọn những mớ rau quá non, mỡ màng, các loại quả to và bóng so với bình thường vì những loại rau quả này người trồng phải dùng hóa chất bảo vệ thực vật, thuốc kích thích tăng trưởng.<br>

-  Không mua rau quả có mùi vị lạ, màu sắc khác thường.<br>

- Nên chọn rau quả còn nguyên lành, không dập nát hoặc có vết nứt, thủng.<br>

- Tránh mua rau quả gọt vỏ và xắt sẵn, ngâm nước vì nguồn nước ngâm không đảm bảo vệ sinh hay có hoà các hoá chất độc hại để giữ trắng.<br>



Cách rửa và chế biến<br>
- Ngâm kỹ, rửa sạch từng lá, nhất là các kẽ lá, gọt vỏ các loại quả ăn tươi. Riêng đối với các loại rau lá nhỏ như xà lách, rau dền, bông cải thì nên pha vào nước rửa 1-2 muỗng cà phê muối để sâu bọ và côn trùng bò ra khỏi kẽ lá. Đối với các loại rau củ, trái cây, để nguyên củ, rửa sạch trước khi gọt vỏ.
<br>
- Rau quả phải ngâm ngập trong nước sạch 15-20 phút, rửa nhiều lần (ít nhất 3-4 lần) dưới vòi nước để loại trừ phần lớn các thuốc bảo vệ thực vật tồn dư.<br>

- Nấu chín và mở vung khi nấu cũng là cách tốt để loại trừ phần lớn dư lượng hoá chất thực vật còn sót lại qua đường bay hơi.<br>
